bool is_close(float a, float b, float tol = 0.02f) {
    return std::abs(a - b) < tol;
}

// Test 1: Multi-Layer Routing (Non-Linearity & DFA Broadcast)
void test_multilayer_routing() {
    // 2 Inputs -> 4 Hidden Neurons -> 1 Output Neuron
    SNN_Crossbar<2, 4> hidden_layer(0.8f, 0.9f, 1.0f, 0.5f, 0.5f, 0.5f, 0.1f);
    SNN_Crossbar<4, 1> output_layer(0.8f, 0.9f, 1.0f, 0.5f, 0.5f, 0.5f, 0.1f);

    bool inputs[2] = {true, false};
    bool hidden_spikes[4] = {false, false, false, false};
    bool final_output[1] = {false};

    // DFA Error broadcast wires
    q8_8_t hidden_dfa[4] = {0, 0, 0, 0};
    q8_8_t output_dfa[1] = {0};

    // Tick 1: Cascade through layers
    hidden_layer.tick(inputs, hidden_spikes, hidden_dfa);
    output_layer.tick(hidden_spikes, final_output, output_dfa);

    std::cout << "[PASS] Multi-Layer Crossbar Cascade (Forward Routing Stable)\n";
}

// TEST 2: Temporal Dependency Benchmark
void test_temporal_memory() {
    SNN_Crossbar<1, 1> core(0.9f, 0.9f, 1.0f, 0.5f, 0.5f, 0.8f, 0.5f);

    core.flash_weight(0, 0, 1.5f);

    bool input[1] = {false};
    bool output[1] = {false};
    q8_8_t no_error[1] = {0};
    q8_8_t error_signal[1] = {Q8_8::from_float(1.0f)};

    float initial_weight = core.probe_weight(0, 0);

    // Tick 1: Fire input
    input[0] = true;
    core.tick(input, output, no_error);

    // Tick 2 & 3: Trace decaying
    input[0] = false;
    core.tick(input, output, no_error);
    core.tick(input, output, no_error);

    // Tick 4: Delayed Error Arrives
    core.tick(input, output, error_signal);

    float new_weight = core.probe_weight(0, 0);

    assert(new_weight > initial_weight);

    std::cout << "[PASS] Temporal Dependency (E-Prop bridges time gap without BPTT)\n";
}

// TEST 3: Hardware Sparsity & Power Profiler
void test_sparsity_profiler() {
    // 16x16 routing core
    SNN_Crossbar<16, 16> core(0.8f, 0.9f, 1.0f, 0.5f, 0.5f, 0.5f, 0.1f);

    bool inputs[16];
    bool outputs[16];
    q8_8_t no_error[16] = {0};

    // 100 ticks
    int total_possible_macs = 16 * 16 * 100;
    int hardware_additions_executed = 0;
    int total_output_spikes = 0;

    // Simulate 100 hardware clock ticks with 10% input activity
    for (int t = 0; t < 100; ++t) {
        for (int i = 0; i < 16; ++i) {
            inputs[i] = (rand() % 100) < 10;
            if (inputs[i]) hardware_additions_executed += 16;
        }

        core.tick(inputs, outputs, no_error);

        for (int i = 0; i < 16; ++i) {
            if (outputs[i]) total_output_spikes++;
        }
    }

    float operation_ratio = (float)hardware_additions_executed / total_possible_macs;
    assert(operation_ratio < 0.20f);

    std::cout << "[PASS] Sparsity Profiler (Bypassed " << (1.0f - operation_ratio) * 100.0f << "% of dense matrix math via conditional routing)\n";
}

// TEST 4: TPU Distillation Bridge
void test_tpu_weight_distillation() {
    SNN_Crossbar<3, 2> core(0.8f, 0.9f, 1.0f, 0.5f, 0.5f, 0.5f, 0.1f);

    // Weights generated by a TPU model
    float tpu_cloud_weights[2][3] = {
        {0.15f, -0.42f, 0.88f},
        {-0.99f, 0.05f, -0.11f}
    };

    // Flash weights to hardware
    for (int out = 0; out < 2; ++out) {
        for (int in = 0; in < 3; ++in) {
            core.flash_weight(out, in, tpu_cloud_weights[out][in]);
        }
    }

    float probed = core.probe_weight(0, 2);
    assert(is_close(probed, 0.88f));

    std::cout << "[PASS] TPU Distillation Bridge (Cloud-to-Edge SRAM Quantization flash successful)\n";
}

